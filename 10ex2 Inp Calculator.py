# Calculator Using User Input (But without If Else)
# All Possible operation is going to be printed
# Float Value is Supported

print("************CALCULATOR************")
print()

a = input("Enter Your first Number : ")
b = input("Enter Your Second Number : ")

print("The Addition of the number is :", float(a) + float(b))
print("The Subtraction of the number is :", float(a) - float(b))
print("The Multiplication of the number is :", float(a) * float(b))
print("The Division of the number is :", float(a) / float(b))
print("The Modulus of the number is :", float(a) % float(b))